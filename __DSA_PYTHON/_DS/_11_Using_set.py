# Creating a set
my_set = {1, 2, 3, 4, 5}
print(my_set)  # Output will vary due to the unordered nature of sets
# op - {1,2,3,4,5}

# Adding elements to a set
my_set.add(6)
print(my_set)  # Output will vary due to the unordered nature of sets
# op -{} 1,2,3,4,5,6}

# Removing elements from a set
my_set.remove(3)
print(my_set)  # Output will vary due to the unordered nature of sets
# op - {1,2,3,5,6}